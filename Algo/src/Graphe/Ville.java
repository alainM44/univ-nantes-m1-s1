package Graphe;

public class Ville
{
String nom;
int qualite;
public Ville(String nom, int qualite)
{
	this.nom = nom;
	this.qualite = qualite;
}
public String getNom()
{
	return nom;
}
public void setNom(String nom)
{
	this.nom = nom;
}
public int getQualite()
{
	return qualite;
}
public void setQualite(int qualite)
{
	this.qualite = qualite;
}

}
